package org.program;

public class NumberPattern {

	public static void main(String[] args) {
		int n = 4;
	    int i,j;
		 for (i = 1; i <= n; i++) {
	            // inner loop to print space
	            for (j = 1; j <=  i; j++) {
	                System.out.print("* ");
	            }
	        
	            
	       System.out.println();
	    
	            
	  }
	}
}        
          
